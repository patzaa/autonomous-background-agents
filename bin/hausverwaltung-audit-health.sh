#!/usr/bin/env zsh
# Hausverwaltung audit-health digest — local automated weekly run.
# Loaded by ~/Library/LaunchAgents/com.hausverwaltung.audit-health.plist
# Fires Monday 08:00 local time (so the digest is fresh when work starts).
#
# Phase-3 of the "make the audit crons effective" work. Two jobs, both read-mostly:
#   1. AUDIT-PR HEALTH — lists every open agent-audit / coverage PR with its age +
#      mergeable/check state, and FLAGS any older than the 3-day TTL. An audit PR
#      that outlives one cycle is a bug in the loop (it's red or conflicted), not a
#      backlog item — this surfaces it so it never sits unnoticed for 10 days again.
#   2. OPENCLAW-ISSUE DEDUP — the openclaw-upgrade-validate cron opens a NEW issue
#      every failed run instead of updating, so exact-title duplicates pile up
#      (35 open at build time). This closes literal duplicates (keeps the NEWEST per
#      title); distinct per-version issues are reported, never auto-closed.
#
# Output: refreshes ONE pinned issue "🩺 Weekly audit health" (find-or-create-edit,
# never a new issue each week — the same one-living-doc discipline) + a log file.

set -uo pipefail

export PATH="/Users/dan/.local/bin:/opt/homebrew/bin:/usr/local/bin:/usr/bin:/bin:$PATH"

REPO="$HOME/hausverwaltung"
LOG="$HOME/Library/Logs/hausverwaltung-audit-health.log"
PINNED_TITLE="🩺 Weekly audit health"
TTL_DAYS=3
BODY_FILE="$(mktemp -t audit-health-body)"
trap 'rm -f "$BODY_FILE"' EXIT

echo ""
echo "=========================================="
echo "  Audit-health digest: $(date -Iseconds)"
echo "=========================================="

if ! curl -sf --max-time 5 https://api.github.com/zen > /dev/null; then
    echo "Skipped: github.com unreachable (likely no network at this hour)."
    exit 0
fi
if [ ! -d "$REPO/.git" ]; then
    echo "ERROR: $REPO is not a git repo" >&2
    exit 1
fi
cd "$REPO"

NOW_EPOCH=$(date +%s)
age_days() {  # ISO-8601 → integer days old (BSD date)
    local created="$1" ce
    ce=$(date -j -f "%Y-%m-%dT%H:%M:%SZ" "$created" +%s 2>/dev/null) || { echo "?"; return; }
    echo $(( (NOW_EPOCH - ce) / 86400 ))
}

{
  echo "## 🩺 Weekly audit health — $(date '+%Y-%m-%d %H:%M %Z')"
  echo ""
  echo "Auto-generated by \`com.hausverwaltung.audit-health\`. One living doc — this issue is rewritten each run, not duplicated."
  echo ""

  # ---- 1. AUDIT-PR HEALTH ------------------------------------------------
  echo "### Open audit PRs (TTL ${TTL_DAYS}d)"
  echo ""
  PRS_JSON=$(gh pr list --state open --limit 100 \
      --json number,title,createdAt,mergeStateStatus,labels,headRefName 2>/dev/null \
    | jq -c '[ .[] | select(
        (.labels // [] | map(.name) | index("agent-audit"))
        or (.headRefName | test("^(coverage|chore/agent-capability-audit|chore/agent-script-audit)/"))
      ) ]')
  COUNT=$(echo "$PRS_JSON" | jq 'length')
  STALE=0
  if [ "$COUNT" = "0" ]; then
      echo "_None open — clean._"
  else
      echo "| PR | Age | Merge state | Title |"
      echo "|----|-----|-------------|-------|"
      echo "$PRS_JSON" | jq -r '.[] | [.number, .createdAt, .mergeStateStatus, .title] | @tsv' \
      | while IFS=$'\t' read -r num created mstate title; do
          d=$(age_days "$created")
          flag=""
          if [ "$d" != "?" ] && [ "$d" -gt "$TTL_DAYS" ]; then flag=" ⚠️ STALE"; fi
          echo "| #${num} | ${d}d${flag} | ${mstate} | ${title} |"
        done
      # recompute stale count (subshell above can't export)
      STALE=$(echo "$PRS_JSON" | jq -r '.[].createdAt' | while read -r c; do age_days "$c"; done | awk -v t="$TTL_DAYS" '$1!="?" && $1>t' | wc -l | tr -d ' ')
  fi
  echo ""
  if [ "${STALE:-0}" -gt 0 ]; then
      echo "> ⚠️ **${STALE} audit PR(s) past the ${TTL_DAYS}-day TTL.** A green audit PR self-merges within the hour (auto-merge is armed), so a stale one is red or conflicted — investigate or close. The next audit run will auto-close >${TTL_DAYS}d PRs."
      echo ""
  fi

  # ---- 2. OPENCLAW-ISSUE DEDUP ------------------------------------------
  echo "### openclaw-upgrade issue hygiene"
  echo ""
  OC_JSON=$(gh issue list --label openclaw-upgrade --state open --limit 200 --json number,title 2>/dev/null)
  TOTAL_OC=$(echo "$OC_JSON" | jq 'length')
  # exact-title duplicates: keep the highest number per title, close the rest
  DUP_NUMS=$(echo "$OC_JSON" | jq -r 'group_by(.title)[] | select(length>1) | (sort_by(.number) | .[:-1][].number)')
  CLOSED=0
  if [ -n "$DUP_NUMS" ]; then
      while read -r n; do
          [ -z "$n" ] && continue
          gh issue close "$n" --comment "Closed as an exact duplicate by the weekly audit-health digest (newest issue with this title kept). Root fix: the openclaw-upgrade-validate cron should update one issue per version instead of opening a new one each failed run." >/dev/null 2>&1 \
            && { echo "- closed duplicate #${n}"; CLOSED=$((CLOSED+1)); }
      done <<< "$DUP_NUMS"
  fi
  [ "$CLOSED" = "0" ] && echo "- no exact-title duplicates to close"
  echo ""
  echo "- ${TOTAL_OC} open \`openclaw-upgrade\` issue(s) before this run; ${CLOSED} exact-duplicate(s) closed."
  # distinct per-version issues referencing an OLD target (reported, NOT auto-closed)
  echo "- Distinct per-version issues are left open (they may carry live diagnostics). If you want a sweep of obsolete-version ones, say so — they're not auto-closed."
  echo ""
  echo "---"
  echo "_Crons: agent-audit (Mon/Wed/Fri/Sun 01:00), coverage (Sun/Wed 03:00), this digest (Mon 08:00)._"
} > "$BODY_FILE"

# ---- 3. Refresh the ONE pinned issue -------------------------------------
EXISTING=$(gh issue list --state open --search "$PINNED_TITLE in:title" --json number,title \
            --jq "[.[] | select(.title==\"$PINNED_TITLE\")][0].number // empty" 2>/dev/null)
if [ -n "$EXISTING" ]; then
    gh issue edit "$EXISTING" --body-file "$BODY_FILE" >/dev/null 2>&1 \
      && echo "Refreshed pinned issue #$EXISTING"
else
    gh label create audit-health --color 1d76db --description "Weekly audit-health digest (one living issue)" >/dev/null 2>&1 || true
    NEW=$(gh issue create --title "$PINNED_TITLE" --body-file "$BODY_FILE" --label audit-health 2>/dev/null | grep -oE '[0-9]+$' | tail -1)
    echo "Created pinned issue #${NEW:-?}"
fi

# Mirror the body into the log for offline review.
{ echo "===== $(date -Iseconds) ====="; cat "$BODY_FILE"; echo; } >> "$LOG"
echo "Digest complete: $(date -Iseconds)"
